import java.util.*;
import java.io.*;
class test{
	public static void main(String[] args) {
		String s1 = "TRIANGLE";
        String s2 ="INTEGRAL";
        int ans1=0,ans2=0;
        for (int i=0;i<s1.length() ;i++ )
        {
            int a=(int)s1.charAt(i);
            ans1=ans1+a;    
        }
        for (int i=0;i<s2.length() ;i++ )
        {
            int a=(int)s2.charAt(i);
            ans2=ans2+a;    
        }
        if(s1.length() == s2.length() && ans1==ans2)
        System.out.print("they are anagrams of each other..");

        else
        System.out.println("they are not anagrams of each other..");       
    }

}
